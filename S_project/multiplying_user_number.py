def multiplying_user_number():
    print("Wellcome to the multiplying user number program!")
    
    user = int(input("Please enter a number: "))
    multiplying = int(input("Please enter a number to multiply it by: "))

    result = user * multiplying

    print(f"The result: {user} multiplied by {multiplying} = {result}")

multiplying_user_number()
