def subtracting_user_number():
    print("Wellcome to the subtracting user number program!")
    
    user = int(input("Please enter a number: "))
    subtracting = int(input("Please enter a number to subtract it by: "))

    result = user - subtracting

    print(f"The result: {user} - {subtracting} = {result}")

subtracting_user_number()
